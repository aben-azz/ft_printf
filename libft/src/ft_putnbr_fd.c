#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		(n == -2147483648) ? ft_putchar_fd('2', fd) : NULL;
		(n == -2147483648) && (n = -147483648);
		n = -n;
	}
	(n > 9) ? ft_putnbr_fd(n / 10, fd) : NULL;
	ft_putchar_fd(48 + n % 10, fd);
}
